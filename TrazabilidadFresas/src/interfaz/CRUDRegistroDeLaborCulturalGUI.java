    private void cargarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) registroDeLaborCulturalTable.getModel();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        registroDeLaboresCulturales = (ArrayList) new RegistroDeLaborCulturalControl().leerTodos();
        for (int i = 0; i < registroDeLaboresCulturales.size(); i++) {
            RegistroDeLaborCultural rc = (RegistroDeLaborCultural) registroDeLaboresCulturales.get(i);
            String fecha = sdf.format(rc.getFecha());
            String[] datos = {fecha, rc.getLaborCultural().getNombre(),
                rc.getFresicultor().getApellidos(),Double.toString(rc.getJornales()),rc.getObservaciones()};
            modelo.addRow(datos);
        }
    }

    private void borrarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) registroDeLaborCulturalTable.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
