    private void borrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarButtonActionPerformed
        // TODO add your handling code here:
        if (registroDeLaborCulturalTable.getSelectedRow() > -1) {
            RegistroDeLaborCultural rc = (RegistroDeLaborCultural) registroDeLaboresCulturales.get(registroDeLaborCulturalTable.getSelectedRow());
            new RegistroDeLaborCulturalControl().eliminar(rc);
            borrarTabla();
            cargarTabla();
        }
        guardarButton.setEnabled(false);
        editing = false;
        RegistroDeLaborCultural rc = new RegistroDeLaborCultural();
        try {
            SimpleDateFormat formatoDelTexto = new SimpleDateFormat("dd/MM/yyyy");
            String strFecha = (String) registroDeLaborCulturalTable.getValueAt(filaEditable, 0);
            strFecha = strFecha.replaceAll(" ", "");
            Date fecha = null;
            fecha = formatoDelTexto.parse(strFecha);
            rc.setFecha(fecha);
            rc.setLaborCultural(null); // Toca ver la forma de que puedan entrar
            rc.setFresicultor(null);    // Toca ver la forma de que puedan entrar
            rc.setJornales(Double.parseDouble((String) registroDeLaborCulturalTable.getValueAt(filaEditable, 3)));
            rc.setObservaciones((String) registroDeLaborCulturalTable.getValueAt(filaEditable, 4));
            if (!new RegistroDeLaborCulturalControl().crear(rc)) {
                throw new Exception();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en los datos", "Error", JOptionPane.ERROR_MESSAGE);
        }
        borrarTabla();
        cargarTabla();
    }//GEN-LAST:event_borrarButtonActionPerformed

